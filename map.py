def generate_grid(gsz=GRIDSIZE):
	# types
	# 1 = edges
	# 2 = background
	# 3 = block
	# 4 = creates upgrades
	# 5 = unkillable block
	# 44 = heart
	# 40 = extrabomb
	grid = [[random.choice([2,2,2,2,2,3,4,5]) for k in range(gsz)] for j in range(gsz)]
	for x in range(gsz):  # set grid edges to solid blocks type 1
		grid[x][0] = 1
		grid[0][x] = 1
		grid[-1][x] = 1
		grid[x][-1] = 1
	# self.grid = grid
	return grid
